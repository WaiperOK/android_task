package com.kizitonwose.calendar.core;

/**
 * Перечисление, определяющее владельца дня (какому месяцу принадлежит день).
 * Это замена оригинального класса DayOwner из библиотеки календаря,
 * который не был найден в версии 2.3.0.
 */
public enum DayOwner {
    /**
     * Дата принадлежит текущему месяцу
     */
    THIS_MONTH,
    
    /**
     * Дата принадлежит предыдущему месяцу
     */
    PREVIOUS_MONTH,
    
    /**
     * Дата принадлежит следующему месяцу
     */
    NEXT_MONTH
} 